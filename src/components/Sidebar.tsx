/** Routes that render their own full-screen chrome (no app nav). */
const HIDE_NAV_PREFIXES = ["/login", "/signup", "/forgot-password", "/reset-password", "/welcome"];
export function isAuthRoute(pathname: string) {
  return HIDE_NAV_PREFIXES.some((p) => pathname === p || pathname.startsWith(p + "/"));
}

/** Desktop side navigation. Hidden below md; BottomNav takes over on mobile. */
export function Sidebar() {
  const pathname = usePathname();

  if (isAuthRoute(pathname)) return null;

  return (
    <aside className="sticky top-0 hidden h-dvh w-60 shrink-0 flex-col border-r border-border bg-surface/40 md:flex">
      <div className="flex items-center gap-2 px-5 py-5">
        <span className="flex h-9 w-9 items-center justify-center rounded-xl bg-primary text-primary-fg">
          <ScanIcon />
        </span>
        <span className="text-lg font-bold tracking-tight text-text">StockDesk</span>
      </div>
      <nav className="flex flex-1 flex-col gap-1 px-3">
        {items.map((it) => {
          const active = isActive(pathname, it.href);
          const Icon = it.icon;
          return (
            <Link
              key={it.href}
              href={it.href}
              className={cn(
                "flex items-center gap-3 rounded-xl px-3 py-2.5 text-sm font-medium transition",
                active
                  ? "bg-primary/15 text-primary"
                  : "text-muted hover:bg-surface-2 hover:text-text"
              )}
            >
              <Icon />
              {it.label}
            </Link>
          );
        })}
      </nav>
    </aside>
  );
}

function isActive(pathname: string, href: string) {
  if (href === "/") return pathname === "/";
  return pathname.startsWith(href);
}

